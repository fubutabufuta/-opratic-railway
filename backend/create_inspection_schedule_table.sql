-- Muayene Programı Tablosu
CREATE TABLE IF NOT EXISTS `inspection_schedule` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `inspection_schedule` int(4) NOT NULL COMMENT 'Muayene yılı',
  `vehicle_type` varchar(10) NOT NULL COMMENT 'Araç tipi (A, B, C, D, E, F)',
  `plate_code` text NOT NULL COMMENT 'Plaka kodları (virgülle ayrılmış)',
  `inspection_date` date NOT NULL COMMENT 'Muayene başlangıç tarihi',
  `inspection_end_date` date NOT NULL COMMENT 'Muayene bitiş tarihi',
  `created_at` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_schedule_type` (`inspection_schedule`, `vehicle_type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

